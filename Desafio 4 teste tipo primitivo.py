algo = input('Digite algo: ')

if(algo.isalpha()):
    print('O que vc digitou é alfabético')
else:
    print('O que vc digitou não é alfabético')
    
if(algo.isnumeric()):
    print('O que vc digitou é um número')
else:
    print('O que vc digitou não é um número')

if(algo.isupper()):
    print('O que vc digitou está em capslock')
else:
    print('O que vc digitou não está em capslock')
    
if(algo.isalnum()):
    print('O que vc digitou é alfa numérico')
else:
    print('O que vc digitou não é alfa numérico')


    
    

